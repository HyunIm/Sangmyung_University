// ======================================================================

#include <uart.h>

// ======================================================================

extern unsigned int fact(unsigned int n);

// ======================================================================

int main(int argc, char *argv[])
{
  int n, f;

  for (n = 0; n <= 9; n++) {
    f = fact(n);
    UART_printf("%d!=%d\n", n, f);
  }

  return 0;
}


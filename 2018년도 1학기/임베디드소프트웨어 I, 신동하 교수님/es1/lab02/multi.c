// ======================================================================
// File: multi.c
// ======================================================================

#include <uart.h>

// ======================================================================

extern unsigned int multi(unsigned int i, unsigned int j);

// ======================================================================

int main(int argc, char *argv[])
{
  int i, j, m;

  for (i = 2; i <= 9; i++) {
    for (j = 1; j <= 9; j++) {
      m = multi(i, j);
      UART_printf("%d*%d=%-2d ", i, j, m);
    }
    UART_printf("\n");
  }

  return 0;
}


#include "uart.h"

// ======================================================================

void goto_and_puts(int x, int y, char *str);

// ======================================================================

int main(void)
{
  UART_clear();

  goto_and_puts(5, 2, "Are you there!");
  goto_and_puts(10, 5, "I am here!");
  goto_and_puts(15, 8, "Please help me!");
  goto_and_puts(0, 11, "");

  return 0;
}

// ======================================================================

void goto_and_puts(int x, int y, char *str)
{
  UART_goto(x, y);
  UART_printf("*(%d,%d) %s", x, y, str);
}

